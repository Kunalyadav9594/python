# Get the value of the first array item.
cars = ["Ford", "Volvo", "BMW"]
x = cars[1]
print(x)

# Modify the value of the first array item.
cars = ["Ford", "Volvo", "BMW"]
cars[0] = "Toyota"

# The length of an array can be found with the len() function.
cars = ["Ford", "Volvo", "BMW"]
x = len(cars)
print(x)