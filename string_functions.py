# Python string capitalize() method
txt = "hello, and welcome to my world"
x = txt.capitalize()
print(x)  # Output: "Hello, and welcome to my world"
# The capitalize() method returns a string where the first character is upper case, and the rest is lower case.

# len() function
txt = "Hello, World!"
x = len(txt)
print(x)  # Output: 13
# The len() function returns the length of a string.(number of characters in the string)

# casefold() method
txt = "Hello, And Welcome To My World"
x = txt.casefold()
print(x)  # Output: "hello, and welcome to my world"
# The casefold() method returns a string where all characters are lower case. It is similar to the lower() method but more aggressive in its conversion.