print("Hello"+" World") # Concatenation of strings
print("Hello" * 3) # Repetition of strings
print("Hello"[0]) # Accessing first character
print("Hello"[-1]) # Accessing last character
print("Hello"[1:4]) # Slicing from index 1 to 3
print("Hello"[1:]) # Slicing from index 1 to end
print("Hello"[:4]) # Slicing from start to index 3
print("Hello"[-3:]) # Slicing from third last to end
print("Hello"[1:-1]) # Slicing from index 1 to second last
print("Hello"[1:-2]) # Slicing from index 1 to third last
print("y " in "Hello") # Checking if 'y' is in "Hello"
print("y" not in "Hello") # Checking if 'y' is not in "Hello"
